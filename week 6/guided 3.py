# menggunakan start dan stop
for i in range(1, 10): # for loop yang menggunakan range 1 sampai 9
    print(i, end=' ') 
print()
# menggunakan start dan stop default
for i in range(10): # for loop yang menggunakan range 0 sampai 9
    print(i, end=' ')
print()
# menggunakan start, stop dan step
for i in range(1, 10, 2): # for loop yang menggunakan range 1 sampai 9 dengan step 2
    print(i, end=' ')
print()