nilai = 54 # Inisialisasi variabel nilai

while True: # Looping tanpa batas
  n = int(input("Masukkan nilai: ")) # Meminta input nilai dari pengguna
  if nilai == n: # Jika nilai yang dimasukkan sama dengan nilai yang sudah ditentukan
    print("Nilai sudah benar") # Menampilkan pesan bahwa nilai sudah benar
    break # break statement untuk keluar dari loop
  print("Nilai salah, silahkan coba lagi")  # Menampilkan pesan bahwa nilai salah