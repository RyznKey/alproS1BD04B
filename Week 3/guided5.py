# casting
print (float(60)) #casting float
print (int (5.8967)) #casting integer

# variabel tanpa casting tipe data
angka1 = input("Masukan Angka pertama : ")
angka2 = input("Masukan Angka kedua   : ")

hasil = angka1+angka2 # operasi penjumlahan

print ("Hasil penjumlahan", angka1, "dan", angka2, "adalah", hasil) # output string

# variabel dengan casting tipe data
angka1 = int(input("Masukan Angka pertama : "))
angka2 = int(input("Masukan Angka kedua   : "))

hasil = angka1+angka2 # operasi penjumlahan

print ("Hasil penjumlahan", angka1, "dan", angka2, "adalah", hasil) # output integer