# Nilai a dan b
a = 5
b = 6

# Operasi matematika
print("a + b =", a + b) # hasil penjumlahan a dan b adalah 11
print("b - a =", b - a) # hasil pengurangan b dan a adalah 1
print("a * b =", a * b) # hasil perkalian a dan b adalah 30
print("a / b =", a / b) # hasil pembagian a dan b adalah 0.8333333333333334