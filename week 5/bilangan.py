# if elif else statement dengan input dari pengguna
bilangan = int(input("Masukkan bilangan : ")) # identifier bilangan dengan input dari pengguna
if bilangan < 0: # jika bilangan kurang dari 0
  print("Bilangan negatif") # tampilkan bilangan negatif
elif bilangan == 0: # jika bilangan sama dengan 0
  print("Bilangan nol") # tampilkan bilangan nol
else: # jika bilangan lebih dari 0
  print("Bilangan positif") # tampilkan bilangan positif