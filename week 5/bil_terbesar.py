x = int(input("Masukkan bilangan X: ")) # identifier bilangan x dengan input dari pengguna
y = int(input("Masukkan bilangan Y: ")) # identifier bilangan y dengan input dari pengguna

if x > y: # jika x lebih besar dari y
  print("Bilangan terbesar adalah X: ", x) # tampilkan bilangan terbesar adalah x : (nilai variabel x)
else: # jika x tidak lebih besar dari y
  print("Bilangan terbesar adalah Y: ", y) # tampilkan bilangan terbesar adalah y : (nilai variabel y)