# Operator aritmatika
# penjumlahan 
print (12+2)
apel,jeruk = 7,9
buah = apel + jeruk
print (buah)
# pengurangan
hutang = 10000
bayar = 5000
sisaHutang = hutang - bayar
print ("sisa hutang anda adalah : " , sisaHutang)
# perkalian
panjang = 16
lebar = 8
luas = panjang * lebar
print (luas)
# pembagian
kue = 16
anak = 4
kuePerAnak = kue / anak
print ("setiap anak akan mendapatkan bagian kue sebanyak : ", kuePerAnak)

# sisa bagi / modulus 
bilangan1 = 14
bilangan2 = 5
hasil = bilangan1 % bilangan2
print ("sisa dari bagian ", bilangan1, " dan ", bilangan2, " adalah ", hasil)

# pangkat 
bilangan3 = 8
bilangan4 = 2
hasilPangkat = bilangan3 ** bilangan4
print (hasilPangkat)

# pembagain bulat 
print (10//3)
# 10 bagi 3 adalah 3.333, karena dibulatkan maka hasil nya adalah 3

# pengaturan nilai 

a = 8
b = 3

# operator penjumlahan
c = a + b 
print (f"hasil : {a} + {b} = {c}")
# operator pengurangan
c = a - b 
print (f"hasil : {a} - {b} = {c}")
# operator perkalian
c = a * b 
print (f"hasil : {a} * {b} = {c}")
# operator pembagian
c = a / b 
print (f"hasil : {a} / {b} = {c}")
# operator sisa bagi
c = a % b 
print (f"hasil : {a} % {b} = {c}")
# operator pemangkatan
c = a ** b 
print (f"hasil : {a} ^ {b} = {c}")